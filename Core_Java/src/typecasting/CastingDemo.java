package typecasting;

public class CastingDemo {
    public static void main(String[] args) {
        int a = 10 ;
        System.out.println("this is integer : "+ a);
        double data = a;
        System.out.println("this  is double : "+ data );
        double num = 10.999;
        int b = (int)num;
        System.out.println(b);
//        String word = (String)num;
    }
}


